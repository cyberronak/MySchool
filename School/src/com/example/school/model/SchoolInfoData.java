package com.example.school.model;

public class SchoolInfoData {
	
	private String s_name;
	private String s_address;
	private String s_state;
	private String s_city;
	private String s_email;
	private String s_mobile_no;
	private String s_logo;
	private String s_info;
	
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	public String getS_address() {
		return s_address;
	}
	public void setS_address(String s_address) {
		this.s_address = s_address;
	}
	public String getS_state() {
		return s_state;
	}
	public void setS_state(String s_state) {
		this.s_state = s_state;
	}
	public String getS_city() {
		return s_city;
	}
	public void setS_city(String s_city) {
		this.s_city = s_city;
	}
	public String getS_email() {
		return s_email;
	}
	public void setS_email(String s_email) {
		this.s_email = s_email;
	}
	public String getS_mobile_no() {
		return s_mobile_no;
	}
	public void setS_mobile_no(String s_mobile_no) {
		this.s_mobile_no = s_mobile_no;
	}
	public String getS_logo() {
		return s_logo;
	}
	public void setS_logo(String s_logo) {
		this.s_logo = s_logo;
	}
	public String getS_info() {
		return s_info;
	}
	public void setS_info(String s_info) {
		this.s_info = s_info;
	}
}
